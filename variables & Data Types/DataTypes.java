public class DataTypes {
    public static void main(String[] args) {
        byte b = 8;
        char ch = 'a';
        boolean var = true;
        int a = 12;
        float c = 12.5f;
        short n = 240;
        System.out.println(b);
        System.out.println(ch);
        System.out.println(var);
        System.out.println(a);
        System.out.println(c);
        System.out.println(n);
    }
}
