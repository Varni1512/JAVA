package Operators;

public class Relation {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        //Relational Operator
        System.out.println("== : " + (a==b));
        System.out.println("!= : " + (a!=b));
        System.out.println("< : " + (a<b));
        System.out.println("> : " + (a>b));
        System.out.println(">= : " + (a>=b));
        System.out.println("<= : " + (a<=b));
    }
}
