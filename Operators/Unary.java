package Operators;

public class Unary {
    public static void main(String[] args) {
        int a = 5;
        int b = 5;
        //Arithmetic Unary Operator
        System.out.println("Pre Increment :"+ (++a));
        System.out.println("Post Increment :"+ (a++));
        System.out.println("Pre Decrement :"+ (--b));
        System.out.println("Post Decrement :"+ (b--));
    }
}
